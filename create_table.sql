CREATE TABLE school_district (
	state varchar(255),
	geoid varchar(100),
	name varchar(255),
	low_grade varchar(10),
	high_grade varchar(10),
	population real,
	housing_units real,
	land_area_meters real,
	water_area_meters real,
	land_area_miles real, 
	water_area_miles real,
	lat numeric,
	lon numeric
);
