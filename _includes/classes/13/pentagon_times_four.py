wn = turtle.Screen()
t = turtle.Turtle()
t.color('blue')
for i in range(5):
    t.forward(20)
    t.left(72)
t.up()
t.forward(40)
t.down()
for i in range(5):
    t.forward(30)
    t.left(72)
t.up()
t.forward(60)
t.down()
for i in range(5):
    t.forward(40)
    t.left(72)
t.up()
t.forward(80)
t.down()
for i in range(5):
    t.forward(50)
    t.left(72)
wn.mainloop()
