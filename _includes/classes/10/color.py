import turtle
wn = turtle.Screen()
t = turtle.Turtle()

wn.bgcolor("green") # change the background color to green
t.color("yellow") # change the color of the pen
t.back(100)
t.pensize(20) # change the size of the pen
t.left(45)
t.color("blue") # change the color of the pen again
t.forward(150)

wn.mainloop()
